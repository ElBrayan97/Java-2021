package facultad;

public class Persona {
	
	private String nombre;
	private String apellido;
	private String dni;
	private int edad; 
	private String rol; 
	
	public Persona(String nombre, String apellido, String dni, int edad, String rol) {
		super();
		this.nombre = nombre;
		this.apellido = apellido;
		this.dni = dni;
		this.edad = edad;
		this.rol = rol;
	}
	
	
}
